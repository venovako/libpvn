PROGRAM MAXMIN
  USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY: REAL32, REAL64, REAL128
  IMPLICIT NONE
  WRITE (*,*) '=== REAL32 ==='
  WRITE (*,*) 'MAX(0,-0)=', MAX(0.0_REAL32, -0.0_REAL32)
  WRITE (*,*) 'MAX(-0,0)=', MAX(-0.0_REAL32, 0.0_REAL32)
  WRITE (*,*) 'MIN(0,-0)=', MIN(0.0_REAL32, -0.0_REAL32)
  WRITE (*,*) 'MIN(-0,0)=', MIN(-0.0_REAL32, 0.0_REAL32)
  WRITE (*,*) '=== REAL64 ==='
  WRITE (*,*) 'MAX(0,-0)=', MAX(0.0_REAL64, -0.0_REAL64)
  WRITE (*,*) 'MAX(-0,0)=', MAX(-0.0_REAL64, 0.0_REAL64)
  WRITE (*,*) 'MIN(0,-0)=', MIN(0.0_REAL64, -0.0_REAL64)
  WRITE (*,*) 'MIN(-0,0)=', MIN(-0.0_REAL64, 0.0_REAL64)
  WRITE (*,*) '=== REAL128 ==='
  WRITE (*,*) 'MAX(0,-0)=', MAX(0.0_REAL128, -0.0_REAL128)
  WRITE (*,*) 'MAX(-0,0)=', MAX(-0.0_REAL128, 0.0_REAL128)
  WRITE (*,*) 'MIN(0,-0)=', MIN(0.0_REAL128, -0.0_REAL128)
  WRITE (*,*) 'MIN(-0,0)=', MIN(-0.0_REAL128, 0.0_REAL128)
  WRITE (*,*) '=== LOGICAL ==='
  WRITE (*,*) ' .TRUE.=', TRANSFER(.TRUE., 1)
  WRITE (*,*) '.FALSE.=', TRANSFER(.FALSE., 0)
END PROGRAM MAXMIN
